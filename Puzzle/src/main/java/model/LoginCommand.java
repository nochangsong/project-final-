package model;

public class LoginCommand {
	
	private String Email;
	private String Certify;
	
	
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public String getCertify() {
		return Certify;
	}
	public void setCertify(String certify) {
		Certify = certify;
	}
}
